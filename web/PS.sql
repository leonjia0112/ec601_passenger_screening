DROP SCHEMA IF EXISTS PASSENGERSCREENING;
CREATE DATABASE PASSENGERSCREENING;
USE PASSENGERSCREENING;

CREATE TABLE IMAGE (
    ID INT NOT NULL AUTO_INCREMENT,
    NAME VARCHAR(120) NOT NULL,
    MD5 VARCHAR(100) UNIQUE,
    P1 VARCHAR(40),
    P2 VARCHAR(40),
    P3 VARCHAR(40),
    P4 VARCHAR(40),
    P5 VARCHAR(40),
    P6 VARCHAR(40),
    P7 VARCHAR(40),
    P8 VARCHAR(40),
    P9 VARCHAR(40),
    P10 VARCHAR(40),
    P11 VARCHAR(40),
    P12 VARCHAR(40),
    P13 VARCHAR(40),
    P14 VARCHAR(40),
    P15 VARCHAR(40),
    P16 VARCHAR(40),
    P17 VARCHAR(40),
    PRIMARY KEY (ID)
);